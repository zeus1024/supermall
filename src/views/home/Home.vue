<template>
  <div id="home" class="wrapper" >
    <nav-bar id="home-nav">
      <div slot="center">购物街</div>
    </nav-bar>
     
    <scroll class="content"
            ref="scroll"
            :probe-type="3"
            :pull-up-load="true"
            @pullingUp="loadMore"
            @scrollPosition="scrollPosition"
    >
    <home-swiper :banners="banners"></home-swiper>
    <home-recommend-view :recommends="recommends"></home-recommend-view>
    <feature-view/>
   <tab-control 
    :titles="['流行', '新款', '精选']"
    @tabClick="tabClick"
    class="tab-control"
    ></tab-control>
   
    <goods-list :goods="showGoods"></goods-list>
    </scroll>
    <back-top @click.native="backTopClick" v-show="isShowBackTop"></back-top>
  </div>
</template>

<script>

import HomeSwiper from './childCpn/HomeSwiper.vue'
import HomeRecommendView from './childCpn/HomeRecommendView.vue'
import FeatureView from './childCpn/FeatureView.vue'


import NavBar from 'components/common/navbar/NavBar.vue'
import Scroll from 'components/common/scroll/Scroll.vue'

import TabControl from 'components/content/tabcontrol/TabControl.vue'
import GoodsList from 'components/content/goodslist/GoodsList.vue'
import BackTop from 'components/content/backTop/BackTop.vue'

import {getHomeMultidata,getHomeGoods} from 'network/home'




export default {
  name: "home",
  components:{HomeSwiper,HomeRecommendView,FeatureView,NavBar,Scroll,TabControl,GoodsList, Scroll,BackTop, BackTop},
  data(){
    return {
      banners:[],
      recommends:[],
      goods:{
        'pop':{page:0,list:[]},
        'new':{page:0,list:[]},
        'sell':{page:0,list:[]},
      },
      currentType:'pop',
      isShowBackTop:false,
    }
  },
  created(){
    this.getHomeMultidata();

    this.getHomeGoods('pop');
    this.getHomeGoods('new');
    this.getHomeGoods('sell');
  
  },
  computed:{
    showGoods(){
      return this.goods[this.currentType].list
    }
  },
  methods:{
    // 事件监听方法
    tabClick(index){
      switch (index) {
        case 0:
          this.currentType = 'pop';
          break;
        case 1:
          this.currentType = 'new';
          break;
        case 2:
          this.currentType = 'sell';
          break;
      }
    },

    
    // 网络请求相关的函数
    getHomeMultidata(){
      getHomeMultidata()
    .then(res => {
      this.banners = res.data.banner.list;
      this.recommends = res.data.recommend.list;
    })
    },
    getHomeGoods(type){
      const page = this.goods[type].page + 1;
      getHomeGoods(type,page)
      .then(res => {
         this.goods[type].list.push(...res.data.list);
        //  处理重复数据
         let newArr = new Set(this.goods[type].list);
         this.goods[type].list = Array.from(newArr);	
        //  
         this.goods[type].page += 1;
         this.$refs.scroll.refresh();
         this.$refs.scroll.finishPullUp();
      })
    },
    // 滚动相关方法
    loadMore(){
      this.getHomeGoods(this.currentType);
    },

    // 返回顶部
    backTopClick(){
      this.$refs.scroll.scrollTo(0,0,500)
    },
    // 获取位置
    scrollPosition(position){
      this.isShowBackTop = (-position.y) > 1000;
    }
  }
}
</script>

